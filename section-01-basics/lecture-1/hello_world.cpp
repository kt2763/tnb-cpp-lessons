// 第1講: C++の基本構文と最初のプログラム
// このプログラムは、C++の最も基本的な構造を示しています

#include <iostream>  // 入出力機能を使うためのヘッダファイル

// main関数: プログラムの開始点
// ゲームで言えば、ゲームが起動したときに最初に実行される部分
int main() 
{
    // std::cout を使って画面に文字を表示する
    // std:: は「標準ライブラリの」という意味
    // << は「出力する」という意味の演算子
    std::cout << "Hello, World!" << std::endl;
    
    // ゲーム開発の例: ゲーム起動時のメッセージ
    std::cout << "ゲームを開始します！" << std::endl;
    std::cout << "勇者が目覚めました..." << std::endl;
    
    // return 0 は「プログラムが正常に終了した」という意味
    // ゲームで言えば、エラーなく終了したことを示す
    return 0;
}

/* 
    このプログラムのポイント:
    1. #include: 他のファイルの機能を使えるようにする
    2. main関数: すべてのC++プログラムに必要な開始点
    3. std::cout: 画面に文字を表示する標準的な方法
    4. std::endl: 改行して出力バッファをフラッシュする
    5. return 0: プログラムの正常終了を示す
*/