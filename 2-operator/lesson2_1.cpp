#include <iostream>
using namespace std;

int main()
{
    int a = 10, b = 3;
    cout << a + b << "\n"; // 13
    cout << a - b << "\n"; // 7
    cout << a * b << "\n"; // 30
    cout << a / b << "\n"; // 3
    cout << a % b << "\n"; // 1

    double x = 10.0, y = 3.0;
    cout << x / y << "\n"; // 3.3333...

    int c = 5;
    c++;
    cout << c << "\n"; // 6
}
